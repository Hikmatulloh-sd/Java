public class Variables {
    public static void main(String[] args){
        // Int 32 
        int myInt;
        myInt = 557;
        // Int 16
        short myShort = 3266;
        // Int 62
        long myLong = 26246446;
        System.out.println(myInt);

        // 34 bit 
        float myFloat = 235.33f;
        // 64 bit 
        double myDouble = 2345.456;

        char a = 'a';

        boolean isFree = true;

        // int 8 
        byte myByte = 100; // -128 127
    }
}
